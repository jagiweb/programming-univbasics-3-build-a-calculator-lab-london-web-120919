def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  subtracts = num1 - num2
  subtracts
end

def division(num1, num2)
  divides = num1/num2
  divides
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  mod = num1%num2
  mod
end

def square_root(num)
  square = num.Math.sqrt
  square
end
